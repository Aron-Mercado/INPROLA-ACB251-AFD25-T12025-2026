public class Person {

    private String name="";
    private int age = 18;
    private String gender = "female";

public void setName(String s) {
    name = s;
}

public String getName() {
    return name;
}

public void setAge(int i) {
    age = i;
}

public int getAge() {
    return age;
}

public void setGender(String s) {
    gender = s;
}

public String getGender() {
    return gender;
}

}
